import math

print(round(1.3))            # Round devuelve el numero mas cercano

print(round(1.5))            # Round devuelve el numero mas cercano

print(round(1.8))            # Round devuelve el numero mas cercano

print(abs(1-77))             # Devuelve el valor absoluto
print(abs(-77))              # Devuelve el valor absoluto

print(abs(55))               # Devuelve el valor absoluto

print(math.ceil(1.1))       # Numero superior entero mas cercano
print(math.floor(1.9999))   # Numero inferior entero mas cercano
print(math.isnan(23))       # El valor corresponde a numero real unicamente
print(math.pow(10, 3))      # Eleva x numero a la potencia de ", y"
print(math.sqrt(9))         # Encuentra la raiz cuadrada
